package com.suixingpay.takin.serializer.autoconfigure;

import org.springframework.boot.autoconfigure.condition.ConditionalOnMissingBean;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import com.suixingpay.takin.serializer.ISerializer;
import com.suixingpay.takin.serializer.hessian.HessianSerializer;

/**
 * @author: jiayu.qiu[qiu_jy@suixingpay.com]
 * @date: 2017年7月19日 下午10:58:32
 * @version: V1.0
 * @review: jiayu.qiu[qiu_jy@suixingpay.com]/2017年7月19日 下午10:58:32
 */
@Configuration
public class SerializerAutoConfiguration {
    @Bean
    @ConditionalOnMissingBean(ISerializer.class)
    public ISerializer sxfSerializer() {
        ISerializer res = new HessianSerializer();
        return res;
    }
}
